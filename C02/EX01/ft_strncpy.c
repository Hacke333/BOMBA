#include <unistd.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;

	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int	main(void)
{
	char	*src;
	char	dest[12];

	src = "Hello World!";
	ft_strncpy(dest, src, 5);
	write(1, dest, 12);
	write(1, "\n", 1);
	return (0);
}
